package Nivel_Inicial;

/**
 * Ejercicio 1: Variables y tipos de datos
 *
 * Escribe un programa Java que declare e inicialice
 * variables de diferentes tipos de datos
 * y luego las imprima en la consola.
 *
 */
public class VariablesTiposDatos {
    public static void main(String[] args) {
        // Declaraciones e inicializacion de variables
        int entero = 10;
        double decimal = 3.14;
        boolean booleano = true;
        char caracter = 'D';
        String cadena = "Hola mundo!";

        // Impresion de variables en consola
        System.out.println("Entero: " + entero);
        System.out.println("Decimal: " + decimal);
        System.out.println("Booleano: " + booleano);
        System.out.println("Caracter: " + caracter);
        System.out.println("Cadena: " + cadena);


    }
}
